<h5>Qu'est-ce que le 4L TROPHY ?</h5></br>

</br>Le 4L Trophy est le plus grand raid humanitaire, solidaire et sportif d’Europe dédié aux étudiants de toute nationalité, âgée de 18 à 28ans ! Chaque année, ce n’est pas moins de 2400 jeunes qui partent à l’aventure sur un parcours de plus de 6000km à travers la France, l’Espagne et le Maroc, le tout à bord de la mythique 4L 
</section>

<section>
Quel est le but ?</br>

</br>Au volant de leur 4L les trophystes devront arpenter le désert afin d'acheminer des fournitures scolaires et sportives aux enfants les plus démunis grâce à l'association "Enfants du Désert". Mais c'est aussi 12 tonnes de denrées alimentaires récolter chaque années au départ, soit plus de 24 000 repas distribués à des familles françaises dans le besoin. 
</section>

